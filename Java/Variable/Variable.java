// Variable in Java

import java.lang.*;

class Variable{
	public static void main(String args[]){
		String Name="Rupan Rs";
		int Age=23;
		float Weight=49.5F;
		boolean Married=true;
		
		//Print 
		System.out.println("Name:"+Name);
		System.out.println("Age:"+Age);
		System.out.println("Weight:"+Weight);
		System.out.println("Married:"+Married);
	}
}